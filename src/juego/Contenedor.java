    @Override
    public void run() {
        if (getNivel() == 1){ 
        while (true) {
                    try {
                        juego1.moverPelota();
                        juego1.moverRaqueta();
                        juego1.repaint();
                        Thread.sleep(5);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Nivel1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                }
        
            }
        
         if (getNivel() == 2) {
             while (true) {
                    try {
                        juego2.moverRaqueta();
                        juego2.moverBloque();  
                        juego2.repaint();
                        Thread.sleep(8);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Nivel1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                } 
        
         } 
         if (getNivel() == 3) { 
             while (juego3.boss.vidas > 0) {
                    try {
                        juego3.moverPelota();
                        juego3.moverRaqueta();
                        juego3.moverBloque();
                        juego3.moverBoss();
                        juego3.moverCuadrado();
                        juego3.repaint();
                        Thread.sleep(10);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Nivel1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                     
                }
       
         }
    } 

